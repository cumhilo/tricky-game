package me.cxmilo.tricky.coordinate;

public interface Coordinate {

    /**
     * Represent the coordinate as a number.
     *
     * @return the coordinate as a number.
     */
    int getNumber();

    /**
     * Get the X coordinate of a number.
     *
     * @return The X coordinate of a number.
     */
    int getX();

    /**
     * Get the Y coordinate of a number.
     *
     * @return The Y coordinate of a number.
     */
    int getY();
}
